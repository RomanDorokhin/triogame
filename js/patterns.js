'use strict';

/**
 * Паттерны по фазам (индекс сегмента % 4).
 * Каждая фаза — свой ритмический характер.
 * Станза: ровные доли, предсказуемо.
 */
const stanza = {
  easy: [
    [0, 1], [2, 0], [4, 2], [6, 1],
    [8, 0], [10, 2], [12, 1], [14, 0],
  ],
  norm: [
    [0, 0], [2, 2], [4, 1], [6, 0],
    [8, 2], [10, 1], [12, 0], [14, 2],
  ],
  hard: [
    [0, 0], [1, 2], [2, 1], [3, 2],
    [4, 0], [5, 1], [6, 2], [7, 0],
    [8, 1], [9, 2], [10, 0], [11, 1],
    [12, 2], [13, 0], [14, 1], [15, 2],
  ],
};

/** Пульс: удары по кругу чуть чаще */
const pulse = {
  easy: [
    [0, 0], [2, 1], [4, 2], [6, 0],
    [8, 1], [10, 2], [12, 0], [14, 1],
  ],
  norm: [
    [0, 1], [1.5, 0], [3, 2], [4.5, 1],
    [6, 0], [7.5, 2], [9, 1], [10.5, 0],
    [12, 2], [13.5, 1], [15, 0],
  ],
  hard: [
    [0, 1], [1, 0], [2, 2], [3, 1],
    [4, 0], [5, 2], [6, 1], [7, 0],
    [8, 2], [9, 1], [10, 0], [11, 2],
    [12, 1], [13, 0], [14, 2], [15, 1],
  ],
};

/** Оффбит: смещение, «провал» в середину */
const offbeat = {
  easy: [
    [1, 0], [3, 2], [5, 1], [7, 0],
    [9, 2], [11, 1], [13, 0], [15, 2],
  ],
  norm: [
    [0.5, 2], [2.5, 0], [4.5, 1], [6.5, 2],
    [8.5, 0], [10.5, 1], [12.5, 2], [14.5, 0],
  ],
  hard: [
    [0.5, 1], [1.5, 2], [2.5, 0], [3.5, 1],
    [4.5, 2], [5.5, 0], [6.5, 1], [7.5, 2],
    [8.5, 0], [9.5, 1], [10.5, 2], [11.5, 0],
    [12.5, 1], [13.5, 2], [14.5, 0], [15.5, 1],
  ],
};

/**
 * Финал: акцент + иногда аккорд (две ноты в один beat).
 * Формат: [beat, lane] или [beat, lane, lane2] для двойного тапа.
 */
const finale = {
  easy: [
    [0, 0], [2, 1], [4, 2], [6, 0, 1],
    [8, 2], [10, 0], [12, 1], [14, 2],
  ],
  norm: [
    [0, 0], [2, 1, 2], [4, 2], [6, 0],
    [8, 1], [10, 0, 2], [12, 2], [14, 1],
  ],
  hard: [
    [0, 0, 1], [1, 2], [2, 0], [3, 1, 2],
    [4, 2], [5, 0], [6, 1], [7, 0, 2],
    [8, 1], [9, 2], [10, 0], [11, 1, 2],
    [12, 2], [13, 0], [14, 1], [15, 0, 2],
  ],
};

const BY_PHASE = [stanza, pulse, offbeat, finale];

/**
 * @param {string} diff — 'easy' | 'norm' | 'hard'
 * @param {number} loopIndex — номер 16-битного цикла с начала песни
 * @returns {Array<[number, number]|[number, number, number]>}
 */
export function getPatternForLoop(diff, loopIndex) {
  const phase = loopIndex % BY_PHASE.length;
  const table = BY_PHASE[phase];
  return table[diff] || table.norm;
}

/** Человекочитаемое имя фазы для HUD */
export function phaseLabel(loopIndex) {
  const names = ['СТАНЗА', 'ПУЛЬС', 'ОФФБИТ', 'ФИНАЛ'];
  return names[loopIndex % names.length];
}
